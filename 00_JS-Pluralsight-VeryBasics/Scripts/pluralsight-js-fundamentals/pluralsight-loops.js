// FOR LOOP
for (let i = 0; i < 5; i++) {
  console.log(i);
}

// WHILE LOOP
let count = 5;
while (count > 0) {
  console.log(count);
  count--;
}

// DO WHILE LOOP
// DO WHILE unlike WHILE loop will loop at least once, cause WHILE is being checked at the end of the cicle
let chislo = 20;
do {
  console.log(chislo);
  chislo++;
} while (chislo < 5);
